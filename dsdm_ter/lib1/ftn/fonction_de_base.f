C     COMMENTAIRES GENERAUX SUR DEBFUN
C 
C ----------------------------------------------------------------------
C 
C     DEBFUN est le fichier des fonctions de forme des elements
C     qui sont des fonctions de Lagrange de degre 2 en z, et des
C     fonctions de degre 3 en r. Les variables reduites son notees
C     X et Y associees a r et z. 
C 
C     De maniere generale les fonctions type lagrange sont notees :                                                       C
C               - LI
C               - LPI(pour leurs derivees reelles(i.e par rapport a z)
C 
C     De maniere generale les fonctions type Hermite sont notees :                                                       C
C               - HI
C               - HPI(pour leurs derivees reelles(i.e par rapport a r)
C 
C     De maniere generale les fonctions de forme sont notees :
C                -NI
C               - NXI(pour leurs derivees reelles(i.e par rapport a r)
C               - NYI(pour leurs derivees reelles(i.e par rapport a z)
C               - NRI(lorsqu'elle sont divisees par r)
C 
      DOUBLE PRECISION FUNCTION L1(Y)
C 
C -----------------------------------------------------------------------
C  
C     DECLARATION DES PARAMETRES LOCAUX
C     """"""""""""""""""""""""""""""""""
C 
      DOUBLE PRECISION Y
C 
      L1=Y*(Y-1.D0)/2.D0
C 
      RETURN
      END
